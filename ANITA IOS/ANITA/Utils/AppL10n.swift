            "Strategist": "level.strategist",
            "Financial Thinker": "level.financial_thinker",
            "Optimizer": "level.optimizer",
            "Money Master": "level.money_master"
        ]
        if let key = keyMap[title] {
            return t(key)
        }
        return title
    }
    
