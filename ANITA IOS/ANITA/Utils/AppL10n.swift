import Foundation

enum AppL10n {
    static let preferredLanguageKey = "anita_preferred_language_code"
    
    /// If user has a saved language (en/de), use it. Otherwise auto-detect from device: de → German, else → English.
    static func currentLanguageCode() -> String {
        if let saved = UserDefaults.standard.string(forKey: preferredLanguageKey) {
            return saved == "de" ? "de" : "en"
        }
        let deviceCode = String((Locale.preferredLanguages.first ?? "en").prefix(2)).lowercased()
        return deviceCode == "de" ? "de" : "en"
    }
    
    static func setLanguageCode(_ code: String) {
        let valid = (code == "de" ? "de" : "en")
        UserDefaults.standard.set(valid, forKey: preferredLanguageKey)
    }
    
    static func localeIdentifier(for code: String) -> String {
        code == "de" ? "de_DE" : "en_US"
    }
    
    static func t(_ key: String, languageCode: String? = nil) -> String {
        let lang = languageCode ?? currentLanguageCode()
        if let value = translations[lang]?[key] {
            return value
        }
        // Fallback to English
        if let value = translations["en"]?[key] {
            return value
        }
        return key
    }
    
    /// Returns localized level title for XP level widget (chat sidebar + finance page). Keys match XPLevelConfig in Models.swift.
